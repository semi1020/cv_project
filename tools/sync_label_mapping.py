def _emit_dict_block(name: str, type_hint: str, items: dict[str, dict]) -> list[str]:
    lines = [f"{name}: {type_hint} = {{\n"]
    for main, entry in items.items():
        lines.append(f"    {main!r}: {{\n")
        lines.append(f'        "dino_prompt": {entry["dino_prompt"]!r},\n')
        lines.append('        "sub_categories": {\n')
        for sk, sv in entry["sub_categories"].items():
            lines.append(f"            {sk!r}: {sv!r},\n")
        lines.append("        },\n")
        lines.append("    },\n")
    lines.append("}\n\n")
    return lines


def render(
    all_categories: dict[str, dict],
    active_main: set[str],
    kor_to_en: dict[str, list[str]],
    active_subs_norm: set[str],
) -> tuple[str, list[str]]:
    out: list[str] = [HEADER]

    out.append("# Full 96-category universe. Sub-string values are CLIP prompts;\n")
    out.append("# sub_categories keys match normalized CSV sub labels.\n")
    out.extend(_emit_dict_block("_ALL_CATEGORIES", "dict[str, dict]", all_categories))

    out.append("# Active main categories — derived from label_mapping.KEEP_MAINS\n")
    out.append("# (mains with at least one sub_category having CSV count >= 100).\n")
    out.append("ACTIVE_MAIN: set[str] = {\n")
    for m in sorted(active_main):
        out.append(f"    {m!r},\n")
    out.append("}\n\n")

    out.append("# Pipeline-facing config: active mains only.\n")
    out.append("# - dino_prompt: built from KOR_TO_EN aliases (label_mapping.py)\n")
    out.append("# - sub_categories: from _ALL_CATEGORIES, filtered to ACTIVE_SUBS\n")
    out.append("CATEGORY_CONFIG: dict[str, dict] = {\n")
    skipped: list[str] = []
    for main in sorted(active_main):
        if main not in all_categories:
            skipped.append(f"{main}: missing from _ALL_CATEGORIES (add a description block)")
            continue
        entry = all_categories[main]
        new_prompt = build_dino_prompt(kor_to_en.get(main, [main.lower()]))
        filtered = {
            sk: sv
            for sk, sv in entry["sub_categories"].items()
            if sk in active_subs_norm
        }
        if not filtered:
            skipped.append(f"{main}: no sub_categories match ACTIVE_SUBS")
            continue
        out.append(f"    {main!r}: {{\n")
        out.append(f'        "dino_prompt": {new_prompt!r},\n')
        out.append('        "sub_categories": {\n')
        for sk, sv in filtered.items():
            out.append(f"            {sk!r}: {sv!r},\n")
        out.append("        },\n")
        out.append("    },\n")
    out.append("}\n")
    return "".join(out), skipped


def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--label-mapping", type=Path, default=DEFAULT_LABEL_MAPPING)
    ap.add_argument("--config", type=Path, default=SEMI_CONFIG)
    ap.add_argument("--out", type=Path, default=SEMI_CONFIG)
    ap.add_argument("--dry-run", action="store_true",
                    help="print rendered config to stdout instead of writing")
    args = ap.parse_args()

    if not args.label_mapping.exists():
        print(f"[error] label_mapping not found: {args.label_mapping}", file=sys.stderr)
        return 1
    if not args.config.exists():
        print(f"[error] semi config not found: {args.config}", file=sys.stderr)
        return 1

    lm = import_label_mapping(args.label_mapping)
    KOR_TO_EN = lm.KOR_TO_EN
    ACTIVE_SUBS = lm.ACTIVE_SUBS
    KEEP_MAINS = lm.KEEP_MAINS
    print(
        f"[info] label_mapping: {len(KOR_TO_EN)} mains total, "
        f"{len(KEEP_MAINS)} active mains, {len(ACTIVE_SUBS)} active subs",
        file=sys.stderr,
    )

    src = args.config.read_text(encoding="utf-8")
    all_cats = parse_all_categories(src)
    print(f"[info] semi config parsed: {len(all_cats)} categories", file=sys.stderr)

    active_subs_norm = {normalize_sub(s) for s in ACTIVE_SUBS}

    semi_subs: set[str] = set()
    for entry in all_cats.values():
        semi_subs.update(entry["sub_categories"].keys())
    missing = sorted(active_subs_norm - semi_subs)
    if missing:
        print(
            f"[warn] {len(missing)} active subs have no description in _ALL_CATEGORIES:",
            file=sys.stderr,
        )
        for s in missing:
            print(f"    {s}", file=sys.stderr)
        print(
            "    -> these subs will be omitted from CATEGORY_CONFIG until added.",
            file=sys.stderr,
        )

    rendered, skipped = render(all_cats, KEEP_MAINS, KOR_TO_EN, active_subs_norm)

    if skipped:
        print(f"[warn] {len(skipped)} active mains skipped:", file=sys.stderr)
        for s in skipped:
            print(f"    {s}", file=sys.stderr)

    if args.dry_run:
        sys.stdout.write(rendered)
        return 0

    args.out.write_text(rendered, encoding="utf-8")
    n_written = rendered.count('"dino_prompt"') - len(all_cats)
    print(f"[done] wrote {args.out}", file=sys.stderr)
    print(
        f"[summary] _ALL_CATEGORIES={len(all_cats)}, "
        f"ACTIVE_MAIN={len(KEEP_MAINS)}, CATEGORY_CONFIG entries={n_written}",
        file=sys.stderr,
    )
    return 0


if __name__ == "__main__":
    sys.exit(main())
