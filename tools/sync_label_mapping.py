"""
Sync ~/cv-project/code/label_mapping.py -> ~/cv-project-semi/config.py.

Inputs
------
  /home/lgai/cv-project/code/label_mapping.py
    KOR_TO_EN     : dict[main_kor, list[en_alias]]   (DINO prompt source)
    ACTIVE_SUBS   : set[sub_label]                   (CSV count >= 100)
    KEEP_MAINS    : set[main_kor]                    (active mains)

  ./config.py (semi)
    CATEGORY_CONFIG with 2 active + 94 commented entries — sub descriptions
    (CLIP-friendly natural language) live here.

Output (overwrites ./config.py)
-------------------------------
  _ALL_CATEGORIES : full 96-category universe (descriptions preserved)
  ACTIVE_MAIN     : KEEP_MAINS (single source of truth)
  CATEGORY_CONFIG : active subset
                    - dino_prompt overridden by KOR_TO_EN aliases
                    - sub_categories filtered to ACTIVE_SUBS (normalized)

Run:
    python tools/sync_label_mapping.py
    python tools/sync_label_mapping.py --dry-run    # print to stdout
"""
from __future__ import annotations

import argparse
import ast
import importlib.util
import re
import sys
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[1]
SEMI_CONFIG = REPO_ROOT / "config.py"
DEFAULT_LABEL_MAPPING = REPO_ROOT / "src" / "label_mapping.py"


def normalize_sub(s: str) -> str:
    """Match semi's sub_category normalization: spaces removed, 'cm' -> '㎝'."""
    return s.replace(" ", "").replace("cm", "㎝")


def import_label_mapping(path: Path):
    spec = importlib.util.spec_from_file_location("_label_mapping_src", path)
    if spec is None or spec.loader is None:
        raise RuntimeError(f"cannot load module from {path}")
    mod = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(mod)
    return mod


def _uncomment_dict_members(src: str) -> str:
    """Strip leading "# " only on commented dict-structure lines.

    A line qualifies if, after removing "# ", it starts with a dict-syntax
    char (string quote, brace, paren, comma). Plain prose comments like
    "# Uncomment to include additional categories:" are left intact, otherwise
    they would unfold into invalid Python.
    """
    out: list[str] = []
    struct_chars = ('"', "'", "{", "}", "(", ")", ",")
    for line in src.splitlines(keepends=True):
        m = re.match(r"^(\s+)#\s?(.*)$", line, flags=re.DOTALL)
        if m:
            indent, rest = m.group(1), m.group(2)
            stripped = rest.lstrip()
            if stripped and stripped[0] in struct_chars:
                out.append(indent + rest)
                continue
        out.append(line)
    return "".join(out)


def _find_dict_assign(tree: ast.Module, name: str):
    for node in tree.body:
        target_id = None
        value = None
        if isinstance(node, ast.AnnAssign) and isinstance(node.target, ast.Name):
            target_id = node.target.id
            value = node.value
        elif isinstance(node, ast.Assign) and len(node.targets) == 1 \
                and isinstance(node.targets[0], ast.Name):
            target_id = node.targets[0].id
            value = node.value
        if target_id == name and value is not None:
            return value
    return None


def parse_all_categories(src: str) -> dict[str, dict]:
    """Parse config.py and return the 96-category universe.

    Lookup order (so re-runs preserve all descriptions):
      1. _ALL_CATEGORIES   — present in regenerated configs (idempotent path)
      2. CATEGORY_CONFIG   — fallback for the first run against semi's
                             original config (96 entries, mostly commented out)
    """
    uncommented = _uncomment_dict_members(src)
    try:
        tree = ast.parse(uncommented)
    except SyntaxError as e:
        lines = uncommented.splitlines()
        lo, hi = max(0, e.lineno - 4), min(len(lines), e.lineno + 3)
        ctx = "\n".join(f"{i+1:4d}: {lines[i]}" for i in range(lo, hi))
        raise RuntimeError(
            f"ast.parse failed at line {e.lineno}: {e.msg}\n"
            f"context:\n{ctx}"
        ) from e

    for name in ("_ALL_CATEGORIES", "CATEGORY_CONFIG"):
        value = _find_dict_assign(tree, name)
        if value is not None:
            return ast.literal_eval(value)
    raise RuntimeError(
        "Neither _ALL_CATEGORIES nor CATEGORY_CONFIG found in config.py"
    )


def build_dino_prompt(aliases: list[str]) -> str:
    parts = [a.lower().strip() for a in aliases if a and a.strip()]
    return " . ".join(parts) + " ."


